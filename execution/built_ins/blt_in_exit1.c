#include "../execution.h"

int	ft_only_digits(char *s)
{
	int	i;

	if (!s)
		return (-1);
	i = 0;
	if (s[i] == '+' || s[i] == '-')
		i++;
	while (s[i] != '\0')
	{
		if (s[i] < '0' || s[i] > '9')
			return (0);
		i++;
	}
	return (1);
}
